# List is a collection of items which is ordered and changeable and allows duplicate items.
# Lists are written within square brackets, []. 

lst=list()          # creates an empty list into the variable "lst"
print(lst)

print(len(lst))     # len gives the len of the list

# A list can contain items of any datatypes.

list1=[1,2,3,4,5]
list2=["Apple","Orange","Banana","Grapes"]
list3=[1,"Apple",2,"Orange",3,"Grapes"]

print(list1,"\t",list2,"\t",list3)          # All the lists are printed with a tab space between them

print(len(list1),"\t",len(list2),"\t",len(list3))  
