#String slicing

proverb="An apple a day keeps a doctor away"
print(proverb[0:9])     #variable[start_index:end_index]

print(proverb[0:len(proverb):3])     #variable[start_index:end_index:step value]

print(proverb[::-1])                 # gives the output in reverse order

